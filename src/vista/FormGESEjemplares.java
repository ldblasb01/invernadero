    private void jButtonPlantarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPlantarActionPerformed
        // TODO add your handling code here:
        if (this.jTableEjemplares.getSelectedRowCount() == 0 || this.jTableEjemplares.getSelectedRowCount() > 1) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un √∫nico Ejemplar de la tabla para plantar.");
        } else {
            int filaSelecc = jTableEjemplares.getSelectedRow();

            Long id = Long.valueOf(jTableEjemplares.getModel().getValueAt(filaSelecc, 0).toString());
            int edad = Integer.valueOf(jTableEjemplares.getModel().getValueAt(filaSelecc, 1).toString());
            DateTimeFormatter dateFormatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
            Date fechaCompra = (java.util.Date) (jTableEjemplares.getModel().getValueAt(filaSelecc, 2));
            int idPlanta = Integer.valueOf(jTableEjemplares.getModel().getValueAt(filaSelecc, 3).toString());
            Ejemplares seleccionado = new Ejemplares(id, edad, fechaCompra, idPlanta);

            FormPlantarEjemplar plantarejemplar = new FormPlantarEjemplar(this, seleccionado);
            plantarejemplar.setVisible(true);
        }
        
        
        
    }//GEN-LAST:event_jButtonPlantarActionPerformed

